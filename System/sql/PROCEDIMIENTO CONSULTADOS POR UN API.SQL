CREATE OR ALTER PROCEDURE DBO.PA_EJECUTA_API (@PROCEDIMIENTO VARCHAR(100), @TOKEN VARCHAR(100), @REQUEST VARCHAR(MAX), @PARAMETROS NVARCHAR(MAX)) AS
BEGIN 
SET NOCOUNT ON
    DECLARE @SESIONVALIDA BIT = 0, @SQL NVARCHAR(MAX) = '', @PARAMDEF NVARCHAR(MAX) =''

    SELECT @PROCEDIMIENTO = ISNULL(@PROCEDIMIENTO, ''), @TOKEN = ISNULL(@TOKEN, '')
    
    IF '' IN(@PROCEDIMIENTO, @TOKEN)
    BEGIN
        SELECT 'Faltan datos' AS MSG, 200 AS RESULT
    END
    BEGIN
        SET @SESIONVALIDA = DBOFN_VALIDA_SESION(@TOKEN)
        
        IF @SESIONVALIDA = 0
        BEGIN
            SELECT 'Sesión no válida' AS MSG, 200 AS RESULT
        END
        ELSE
        BEGIN
            SET @PROCEDIMIENTO = 'PA_' + @PROCEDIMIENTO 
            SET @SQL = 'EXEC DBO.' + @PROCEDIMIENTO + ' @REQ, @PARAM'
            SET @PARAMDEF = N'@REQ VARCHAR(MAX), @PARAM NVARCHAR(MAX)'
            EXEC SP_EXECUTESQL @SQL, @PARAMDEF, @REQUEST, @PARAMETROS
        END
    END

SET NOCOUNT OFF

END
GO


CREATE OR ALTER FUNCTION [dbo].[FN_VALIDA_SESION] (@TOKEN VARCHAR(64)) 
RETURNS BIT
AS
BEGIN
    DECLARE @VALIDO BIT = 0;

    SELECT @VALIDO = 1 
    FROM SESIONES WITH(NOLOCK) 
    WHERE TKSESION = @TOKEN 
      AND ESTATUS = 1 
      AND FECHA_EXPIRACION >= GETDATE();

    RETURN @VALIDO;
END
GO


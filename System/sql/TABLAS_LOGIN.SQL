CREATE TABLE USUARIOS(
    IDUSUARIO INT NOT NULL IDENTITY(1,1) CONSTRAINT PK_USUARIOS_IDUSUARIO PRIMARY KEY,
    TKUSUARIO UNIQUEIDENTIFIER NOT NULL 
        CONSTRAINT DF_USUARIOS_TKUSUARIO DEFAULT NEWID()
        CONSTRAINT UQ_USUARIOS_TKUSUARIO UNIQUE,
    IDSUPERVISOR INT CONSTRAINT FK_USUARIOS_USUARIOS_IDSUPERVISOR FOREIGN KEY REFERENCES USUARIOS(IDUSUARIO),
    NOMBRE VARCHAR(100),
    APELLIDOS VARCHAR(100),
    USUARIO VARCHAR(100) NOT NULL CONSTRAINT UQ_USUARIOS_USUARIO UNIQUE,
    CONTRASENA VARBINARY(64) NOT NULL,
    CORREO VARCHAR(100) NOT NULL CONSTRAINT UQ_USUARIOS_CORREO UNIQUE,
    TELEFONO VARCHAR(100),
    INTENTOS INT NOT NULL CONSTRAINT DF_USUARIOS_INTENTOS DEFAULT 0,
    ESTATUS BIT NOT NULL CONSTRAINT DF_USUARIOS_ESTATUS DEFAULT 1,
    FECHA_CREACION DATETIME NOT NULL CONSTRAINT DF_USUARIOS_FECHA_CREACION DEFAULT GETDATE(),
    FECHA_MODIFICACION DATETIME NOT NULL CONSTRAINT DF_USUARIOS_FECHA_MODIFICACION DEFAULT GETDATE()
)

CREATE TABLE SESIONES(
    IDSESION INT NOT NULL IDENTITY(1,1) CONSTRAINT PK_SESIONES_IDSESION PRIMARY KEY,
    TKSESION UNIQUEIDENTIFIER NOT NULL 
        CONSTRAINT DF_SESIONES_TKSESION DEFAULT NEWID()
        CONSTRAINT UQ_SESIONES_TKSESION UNIQUE, -- Asegúrate que después de UNIQUE no haya una coma extra si la línea sigue abajo
    IDUSUARIO INT CONSTRAINT FK_SESIONES_USUARIOS_IDUSUARIO FOREIGN KEY REFERENCES USUARIOS(IDUSUARIO),
    ESTATUS BIT NOT NULL CONSTRAINT DF_SESIONES_ESTATUS DEFAULT 1,
    FECHA_CREACION DATETIME NOT NULL CONSTRAINT DF_SESIONES_FECHA_CREACION DEFAULT GETDATE(),
    FECHA_MODIFICACION DATETIME NOT NULL CONSTRAINT DF_SESIONES_FECHA_MODIFICACION DEFAULT GETDATE(),
    FECHA_EXPIRACION DATETIME NOT NULL CONSTRAINT DF_SESIONES_FECHA_EXPIRACION DEFAULT DATEADD(day, 1, GETDATE())
)

CREATE TABLE LOGS(
    IDLOG INT NOT NULL IDENTITY(1,1) CONSTRAINT PK_LOGS_IDLOG PRIMARY KEY,
    TKLOG UNIQUEIDENTIFIER NOT NULL 
        CONSTRAINT DF_LOGS_TKLOG DEFAULT NEWID()
        CONSTRAINT UQ_LOGS_TKLOG UNIQUE,
    IDUSUARIO INT CONSTRAINT FK_LOGS_USUARIOS_IDUSUARIO FOREIGN KEY REFERENCES USUARIOS(IDUSUARIO),
    [LOG] VARCHAR(MAX) NOT NULL,
    FECHA_CREACION DATETIME NOT NULL CONSTRAINT DF_LOGS_FECHA_CREACION DEFAULT GETDATE(),
    FECHA_MODIFICACION DATETIME NOT NULL CONSTRAINT DF_LOGS_FECHA_MODIFICACION DEFAULT GETDATE()
)